# Initialize shirts folded number
shirts_folded = 0;

# Using a while look to "fold" shirts until five are folded
while shirts_folded < 5:
    # Increment shirts folded
    shirts_folded += 1

    # Print the number of shirts folded
    print("You have folded", shirts_folded, "shirts.")

# Print a message when all shirts are folded
print("All shirts are folded!")